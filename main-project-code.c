void LIN_t()
{
	for(int L_l=0; L_l<1;)
	{
		/*first sensor*/
		HAL_ADC_PollForConversion(&hadc1, 1000);
		read_first=HAL_ADC_GetValue(&hadc1);
		/*sec sensor*/
		HAL_ADC_PollForConversion(&hadc2, 1000);
		read_first=HAL_ADC_GetValue(&hadc2);
		Ultra_Sonic();
		int mo=0;
		if(Distance<35||mo==2)
		{
			mo=2;
			if(read_first<1000&&read_sec<1000)
			{
				motor_backward();
			}
			if(read_first>1000&&read_sec<1000)
			{
				motor_right();
			}
			if(read_first<1000&&read_sec>1000)
			{
				motor_left();
			}
			if(read_first>1000&&read_sec>1000)
			{
				motor_stop();
			}

		}
		if(Distance>35&&mo==0)
		{
			if(read_first<1000&&read_sec<1000)
			{
				motor_forward();
			}
			if(read_first>1000&&read_sec<1000)
			{
				motor_left();
			}
			if(read_first<1000&&read_sec>1000)
			{
				motor_right();
			}
			if(read_first>1000&&read_sec>1000)
			{
				motor_stop();
			}
		}
		HAL_UART_Receive(&huart1, receivedData_2, 1, HAL_MAX_DELAY);
		if(receivedData_2[0]=='7')
		{
			L_l=1;
		}
		else
		{

		}
	}

}
